package gosomeapi

func Sum(a, b int) int {
	return a + b
}

func Sub(a, b int) int {
	return a - b
}

func Div(a, b int) int {
	return a / b
}

func Mul(a, b int) int {
	return a * b
}

func SumF(a, b float32) float32 {
	return a + b
}

func SubF(a, b float32) float32 {
	return a - b
}

func DivF(a, b float32) float32 {
	return a / b
}

func MulF(a, b float32) float32 {
	return a * b
}
